package modelo;

public class Equipo {
	// atributo de la clase Equipo
	private String nombre;

	// Constructores
	public Equipo() {
	}

	public Equipo(String nombre) {
		this.nombre = nombre;
	}

	// Getter and Setter
	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
